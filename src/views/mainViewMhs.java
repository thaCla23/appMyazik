    private void btnRequestIKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestIKActionPerformed
        String username;
        String waktukeluar;
        String waktukembali;
        String tujuan;
        String alasan;
        
        username = ConstUtil.auth.getUsername();
        waktukeluar = txtWaktuBerangkat.getText();
        waktukembali = txtWaktukembali.getText();
        tujuan = txtTujuan.getText();
        alasan = txtAlasan.getText();

        Daftar daftar = new Daftar(0, username, waktukeluar, waktukembali, tujuan, alasan,"", "");
        cekRequest();
        if(dc.insert(daftar)){
            JOptionPane.showMessageDialog(this,"Berhasil Request","Pesan",JOptionPane.PLAIN_MESSAGE);
            mainPanel.removeAll();
            mainPanel.repaint();
            mainPanel.revalidate();

            mainPanel.add(requestPanel);
            mainPanel.repaint();
            mainPanel.revalidate();
        }
        
    }//GEN-LAST:event_btnRequestIKActionPerformed

