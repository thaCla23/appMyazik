    private void cekRequest(){
        try{
            if (txtWaktuBerangkat.getText().equals("") || txtWaktukembali.getText().equals("") || txtAlasan.getText().equals("")||txtTujuan.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Data Tidak Boleh Kosong","pesan",JOptionPane.ERROR_MESSAGE);
            }
            
            else{
                st = con.createStatement();
                String simpan = "insert into daftarik (username, waktu_pergi, waktu_kembali, tujuan, alasan) values('"+ConstUtil.auth.getUsername()+"','"+txtWaktuBerangkat.getText()+"','"+txtWaktukembali.getText()+"','"+txtTujuan.getText()+"','"+txtAlasan.getText()+"')";
                st=con.createStatement();
                int SA= st.executeUpdate(simpan);
                JOptionPane.showMessageDialog(this,"Berhasil request");
                }
        }
        
        catch(Exception e){
            mainPanel.removeAll();
            mainPanel.repaint();
            mainPanel.revalidate();

            // add panel

            mainPanel.add(requestPanel);
            mainPanel.repaint();
            mainPanel.revalidate();
        }
    }
    
